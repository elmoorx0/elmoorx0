function templateDataTable(name: string): string {
  return `/**
 * ${name} — Generated by Elmoorx AI Copilot
 * Sortable data table.
 */

import { $store, h, type ElmoorxNode } from "@elmoorx/runtime";

interface Row {
  id: number;
  name: string;
  email: string;
  role: string;
}

const ${name} = () => {
  const store = $store<{
    rows: Row[];
    sortKey: keyof Row | null;
    sortDir: "asc" | "desc";
  }>({
    rows: [
      { id: 1, name: "Alice", email: "alice@example.com", role: "Admin" },
      { id: 2, name: "Bob", email: "bob@example.com", role: "User" },
      { id: 3, name: "Carol", email: "carol@example.com", role: "Editor" },
    ],
    sortKey: null,
    sortDir: "asc",
  });

  const sorted = () => {
    if (!store.sortKey) return store.rows;
    const key = store.sortKey;
    const dir = store.sortDir === "asc" ? 1 : -1;
    return [...store.rows].sort((a, b) => {
      if (a[key] < b[key]) return -1 * dir;
      if (a[key] > b[key]) return 1 * dir;
      return 0;
    });
  };

  const sortBy = (key: keyof Row) => {
    if (store.sortKey === key) {
      store.sortDir = store.sortDir === "asc" ? "desc" : "asc";
    } else {
      store.sortKey = key;
      store.sortDir = "asc";
    }
  };

  return h(
    "table",
    { class: "data-table" },
    h("thead", null,
      h("tr", null,
        ...(["id", "name", "email", "role"] as const).map((k) =>
          h("th", { onClick: () => sortBy(k), class: "sortable" }, k)
        )
      )
    ),
    h("tbody", null,
      () => sorted().map((row) =>
        h("tr", { key: String(row.id) },
          h("td", null, String(row.id)),
          h("td", null, row.name),
          h("td", null, row.email),
          h("td", null, row.role)
        )
      )
    )
  );
};

export default function Page(): ElmoorxNode {
  return h("main", null, ${name}());
}
`;
}

function templateSearch(name: string): string {
  return `/**
 * ${name} — Generated by Elmoorx AI Copilot
 * Debounced search with results.
 */

import { $state, $effect, h, type ElmoorxNode } from "@elmoorx/runtime";

const ${name} = () => {
  const query = $state("");
  const results = $state<string[]>([]);
  const loading = $state(false);
  let debounceTimer: any = null;

  $effect(() => {
    const q = query();
    if (debounceTimer) clearTimeout(debounceTimer);
    if (!q.trim()) {
      results.set([]);
      return;
    }
    loading.set(true);
    debounceTimer = setTimeout(async () => {
      // Replace with your actual search API
      const res = await fetch(\`/api/search?q=\${encodeURIComponent(q)}\`);
      const data = await res.json();
      results.set(data.results || []);
      loading.set(false);
    }, 300);
  });

  return h(
    "div",
    { class: "search" },
    h("input", {
      type: "search",
      placeholder: "Search...",
      value: () => query(),
      onInput: (e: Event) => query.set((e.target as HTMLInputElement).value),
    }),
    () => loading() ? h("div", null, "Searching...") : null,
    h("ul", null,
      () => results().map((r, i) => h("li", { key: String(i) }, r))
    )
  );
};

export default function Page(): ElmoorxNode {
  return h("main", null, ${name}());
}
`;
}

