function templateTodoList(name: string): string {
  return `/**
 * ${name} — Generated by Elmoorx AI Copilot
 * Reactive todo list with filters and persistence.
 */

import { $store, $effect, h, type ElmoorxNode } from "@elmoorx/runtime";

interface Todo {
  id: number;
  text: string;
  done: boolean;
}

interface State {
  todos: Todo[];
  text: string;
  filter: "all" | "active" | "completed";
  nextId: number;
}

const ${name} = () => {
  const store = $store<State>({
    todos: [],
    text: "",
    filter: "all",
    nextId: 1,
  });

  // Persist to localStorage
  $effect(() => {
    const saved = localStorage.getItem("elmoorx-todos");
    if (saved) {
      const parsed = JSON.parse(saved);
      store.todos = parsed.todos || [];
      store.nextId = parsed.nextId || 1;
    }
  });
  $effect(() => {
    localStorage.setItem("elmoorx-todos", JSON.stringify({
      todos: store.todos,
      nextId: store.nextId,
    }));
  });

  const addTodo = () => {
    const text = store.text.trim();
    if (!text) return;
    store.todos.push({ id: store.nextId, text, done: false });
    store.nextId += 1;
    store.text = "";
  };

  const toggle = (id: number) => {
    const todo = store.todos.find((t) => t.id === id);
    if (todo) todo.done = !todo.done;
  };

  const remove = (id: number) => {
    const i = store.todos.findIndex((t) => t.id === id);
    if (i >= 0) store.todos.splice(i, 1);
  };

  const filtered = () => {
    if (store.filter === "active") return store.todos.filter((t) => !t.done);
    if (store.filter === "completed") return store.todos.filter((t) => t.done);
    return store.todos;
  };

  const remaining = () => store.todos.filter((t) => !t.done).length;

  return h(
    "div",
    { class: "todo-app" },
    h("h1", null, "Todos"),
    h("div", { class: "input-row" },
      h("input", {
        type: "text",
        placeholder: "What needs to be done?",
        value: () => store.text,
        onInput: (e: Event) => store.text = (e.target as HTMLInputElement).value,
        onKeyDown: (e: KeyboardEvent) => { if (e.key === "Enter") addTodo(); },
      }),
      h("button", { onClick: addTodo }, "Add")
    ),
    h("div", { class: "filters" },
      (["all", "active", "completed"] as const).map((f) =>
        h("button", {
          class: store.filter === f ? "filter-btn active" : "filter-btn",
          onClick: () => store.filter = f,
        }, f)
      )
    ),
    h("ul", { class: "todo-list" },
      () => filtered().map((todo) =>
        h("li", { class: todo.done ? "todo-item done" : "todo-item", key: String(todo.id) },
          h("input", {
            type: "checkbox",
            checked: todo.done,
            onChange: () => toggle(todo.id),
          }),
          h("span", { class: "todo-text" }, todo.text),
          h("button", { class: "del-btn", onClick: () => remove(todo.id) }, "×")
        )
      )
    ),
    h("div", { class: "footer" },
      () => \`\${remaining()} of \${store.todos.length} remaining\`
    )
  );
};

export default function Page(): ElmoorxNode {
  return h("main", { class: "page" }, ${name}());
}
`;
}

