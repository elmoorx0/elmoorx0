/**
 * Elmoorx AI Copilot — `elmoorx generate "<description>"`
 * ============================================
 * Generates a production-ready, secure Elmoorx component from a
 * natural language description. Uses a local template engine
 * (in production, would call an LLM API).
 *
 *   $ elmoorx generate "login form"
 *   → creates src/LoginForm.elmoorx.tsx
 *
 *   $ elmoorx generate "todo list with filters"
 *   → creates src/TodoList.elmoorx.tsx
 */

import { writeFile, mkdir } from "node:fs/promises";
import { existsSync } from "node:fs";
import { join } from "node:path";

export interface GenerateOptions {
  description: string;
  outDir?: string;
  name?: string;
  // Optional LLM API key for AI-powered generation (falls back to templates)
  apiKey?: string;
}

/**
 * Main entry — `elmoorx generate "login form"`
 */
export async function generate(opts: GenerateOptions): Promise<void> {
  const outDir = opts.outDir || "src";
  const name = opts.name || toPascalCase(extractSubject(opts.description));

  console.warn(`\n  Elmoorx AI Copilot — generating component\n`);
  console.warn(`  Description: "${opts.description}"`);
  console.warn(`  Component:   ${name}\n`);

  // Try LLM first, fall back to template engine
  let code: string;
  if (opts.apiKey) {
    try {
      code = await generateWithLLM(opts.description, name, opts.apiKey);
      console.warn("  ✓ Generated via LLM");
    } catch (_err) {
      console.warn("  ⚠ LLM failed, falling back to template engine");
      code = generateFromTemplate(opts.description, name);
    }
  } else {
    code = generateFromTemplate(opts.description, name);
    console.warn("  ✓ Generated via template engine (set OPENAI_API_KEY for AI mode)");
  }

  // Write file
  if (!existsSync(outDir)) await mkdir(outDir, { recursive: true });
  const filePath = join(outDir, `${name}.elmoorx.tsx`);
  await writeFile(filePath, code);

  console.warn(`\n  ✓ Created: ${filePath}`);
  console.warn(`  ✓ Size:    ${code.length} bytes`);
  console.warn(`  ✓ Secure:  auto-sanitized + CSP-compliant\n`);
}

/**
 * Extract the subject noun from a description.
 *   "login form" → "LoginForm"
 *   "todo list with filters" → "TodoList"
 */
function extractSubject(desc: string): string {
  const words = desc.toLowerCase().trim().split(/\s+/);
  // Stop at prepositions/conjunctions
  const stop = new Set(["with", "and", "for", "that", "which", "to", "in", "on"]);
  const subject: string[] = [];
  for (const w of words) {
    if (stop.has(w)) break;
    subject.push(w);
  }
  return subject.join(" ") || "component";
}

function toPascalCase(s: string): string {
  return s
    .split(/\s+/)
    .map((w) => w.charAt(0).toUpperCase() + w.slice(1))
    .join("");
}

/**
 * Template-based generator — recognizes common patterns.
 * In production, this would be a fallback when no LLM is available.
 */
function generateFromTemplate(description: string, name: string): string {
  const desc = description.toLowerCase();

  // Match against known patterns
  if (desc.includes("login") || desc.includes("sign in") || desc.includes("signin")) {
    return templateLoginForm(name);
  }
  if (desc.includes("todo") || desc.includes("task")) {
    return templateTodoList(name);
  }
  if (desc.includes("counter")) {
    return templateCounter(name);
  }
  if (desc.includes("form") && (desc.includes("contact") || desc.includes("email"))) {
    return templateContactForm(name);
  }
  if (desc.includes("chart") || desc.includes("graph")) {
    return templateChart(name);
  }
  if (desc.includes("modal") || desc.includes("dialog")) {
    return templateModal(name);
  }
  if (desc.includes("navbar") || desc.includes("navigation") || desc.includes("header")) {
    return templateNavbar(name);
  }
  if (desc.includes("table") || desc.includes("data table") || desc.includes("grid")) {
    return templateDataTable(name);
  }
  if (desc.includes("search")) {
    return templateSearch(name);
  }
  if (desc.includes("dropdown") || desc.includes("select")) {
    return templateDropdown(name);
  }

  // Default: generic component
  return templateGeneric(name, description);
}

